
public class MultiDimArrayDemo {

	public void DimArr() {
		String[][] names = {
				{ "Dr. ", "Puan. ", "Tuan. "},
				{"Azman", "Erina"}
				
		};
		System.out.println(names[0][0] + names [1][0]);
        System.out.println(names[0][1] + names [1][1]);
        System.out.println(names[0][2] + names [1][0]);
	}
}